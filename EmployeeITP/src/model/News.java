package model;

public class News {
		
			private String nid;
			private String title;
			private String ne;
			
			public String getNid() {
				return nid;
			}
			public void setNid(String nid) {
				this.nid = nid;
			}
			public String getTitle() {
				return title;
			}
			public void setTitle(String title) {
				this.title = title;
			}
			public String getNe() {
				return ne;
			}
			public void setNe(String ne) {
				this.ne = ne;
			}
			
			
}
